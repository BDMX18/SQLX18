-- 01
SELECT *
FROM
  (SELECT EMP.*, ROWNUM AS "SLNO"
  FROM EMP)
WHERE SLNO < 6;

-- 02
SELECT ENAME, SAL, SLNO
FROM
  (SELECT ENAME, SAL, ROWNUM AS "SLNO"
  FROM (
    SELECT *
    FROM EMP
    WHERE SAL > 5000
  ))
WHERE SLNO < 11;

-- 03
SELECT *
FROM
  (SELECT ENAME, SAL, ROWNUM AS "SLNO"
  FROM
  (
    SELECT *
    FROM EMP
    ORDER BY SAL DESC
  ))
WHERE SLNO < 4;

-- 04
SELECT *
FROM
  (SELECT ENAME, SAL, ROWNUM AS "SLNO"
  FROM
  (
    SELECT *
    FROM EMP
    ORDER BY SAL DESC
  ))
WHERE SLNO > 5 AND SLNO < 11;

-- 05
SELECT *
FROM
  (SELECT ENAME, DEPTNO, ROWNUM AS "SLNO"
  FROM
  (
    SELECT *
    FROM EMP
    WHERE DEPTNO = 90
  ))
WHERE SLNO < 8;

-- 06
SELECT *
FROM
  (SELECT ENAME, HIREDATE, ROWNUM AS "SLNO"
  FROM 
  (
    SELECT *
    FROM EMP
    WHERE HIREDATE > '01-01-2005'
  ))
WHERE SLNO < 6;

-- 07
SELECT *
FROM
  (SELECT ENAME, JOB, ROWNUM AS "SLNO"
  FROM
  (
    SELECT *
    FROM EMP
    WHERE JOB = 'SA_REP'
  ))
