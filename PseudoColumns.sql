-- 01
SELECT *
FROM
  (SELECT EMP.*, ROWNUM AS "SLNO"
  FROM EMP)
WHERE SLNO < 6;

-- 02
SELECT ENAME, SAL, SLNO
FROM
  (SELECT ENAME, SAL, ROWNUM AS "SLNO"
  FROM (
    SELECT *
    FROM EMP
    WHERE SAL > 5000
  ))
WHERE SLNO < 11;

-- 03
SELECT *
FROM
  (SELECT ENAME, SAL, ROWNUM AS "SLNO"
  FROM
  (
    SELECT *
    FROM EMP
    ORDER BY SAL DESC
  ))
WHERE SLNO < 4;

