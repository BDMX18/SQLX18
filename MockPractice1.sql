-- 01
SELECT
E1.ENAME AS "EMPLOYEE NAME",
E2.ENAME AS "MANAGER NAME"
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.JOB = 'CLERK';

-- 02
SELECT
E1.ENAME AS "EMPLOYEE NAME",
E2.JOB AS "MANAGER'S JOB"
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E2.DEPTNO IN (10, 20);

-- 03
SELECT
E1.ENAME, E2.SAL
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.SAL >  2300 AND E2.SAL >  2300;

-- 4
SELECT
E1.ENAME, E2.HIREDATE
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.HIREDATE < '01-01-1982';

-- 5
SELECT
E1.ENAME, E2.COMM
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.JOB = 'SALESMAN' AND E2.DEPTNO = 30;

-- 6
SELECT E1.ENAME, E1.SAL, E2.ENAME, E2.SAL
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.SAL > E2.SAL;

-- 7
SELECT E1.ENAME, E1.HIREDATE, E2.ENAME, E2.HIREDATE
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E2.HIREDATE < E1.HIREDATE;

-- 8
SELECT E1.ENAME, E2.ENAME
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.JOB = E2.JOB;

-- 9
SELECT E1.ENAME, E2.ENAME
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E2.JOB = 'MANAGER';

-- 10
SELECT E1.ENAME, E1.SAL*12, E2.ENAME, E2.SAL*12
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E1.DEPTNO IN (10, 20)
AND E2.SAL > E1.SAL;

-- 11
SELECT E1.ENAME, E2.JOB
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO;

-- 12
SELECT E1.ENAME, E2.SAL
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND E2.SAL LIKE '%50';

SELECT E1.ENAME, E2.SAL
FROM EMP E1, EMP E2
WHERE E1.MGR = E2.EMPNO
AND SUBSTR(E2.SAL, -2, 2) = '50';